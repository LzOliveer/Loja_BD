        sl.setCod_func(Integer.parseInt(cod_func.getText()));
        sl.setData_ini(dt_ini.getText());
        sl.setData_fim(dt_fim.getText());
        sl.setValor(vlr.getText());

        if ((cod_sal.getText().isEmpty()) || (cod_func.getText().isEmpty()) || (dt_ini.getText().isEmpty()) || (dt_fim.getText().isEmpty()) || (vlr.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Os campos não podem estar vazios. Favor preencher todos os campos, selecionar um funcionário e o salario cadastrado", "Aviso - Ajuste de Salário", JOptionPane.INFORMATION_MESSAGE, aviso);
        } else {

            try {

                SalarioController sc = new SalarioController();
                sc.edita(sl);
                lista_func();
                lista_salario();
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(frmLoja.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
