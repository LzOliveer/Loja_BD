    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Salario sl = new Salario();
        sl.setCod_func(Integer.parseInt(cod_func.getText()));
        sl.setData_ini(dt_ini.getText());
        sl.setData_fim(dt_fim.getText());
        sl.setValor(vlr.getText());
        int select = tabfunc.getSelectedRow();
        sl.setNome(tabfunc.getModel().getValueAt(select, 1).toString());

        if ((cod_func.getText().isEmpty()) || (dt_ini.getText().isEmpty()) || (dt_fim.getText().isEmpty()) || (vlr.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Os campos não podem estar vazios. Favor preencher todos os campos e selecionar um funcionário.", "Aviso - Ajuste de Salário", JOptionPane.INFORMATION_MESSAGE, aviso);
        } else {

            try {

                SalarioController sc = new SalarioController();
                sc.cadastra(sl);
                lista_func();
                lista_salario();
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(frmLoja.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

