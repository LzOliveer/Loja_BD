    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Salario sl = new Salario();
        sl.setCod_sal(Integer.parseInt(cod_sal.getText()));
        sl.setCod_func(Integer.parseInt(cod_func.getText()));
        if ((cod_sal.getText().isEmpty()) || (cod_func.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Os campos não podem estar vazios. Favor preencher todos os campos e selecionar um funcionário", "Aviso - Ajuste de Salário", JOptionPane.INFORMATION_MESSAGE, aviso);
        } else {

            try {

                SalarioController sc = new SalarioController();
                sc.exclui(sl);
                cod_sal.setText("");
                cod_func.setText("");
                dt_ini.setText("");
                dt_fim.setText("");
                vlr.setText("");
                pesqfunc.setText("");
                lista_func();
                lista_salario();
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(frmLoja.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed


